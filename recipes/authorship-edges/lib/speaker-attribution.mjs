/**
 * speaker-attribution.mjs — deterministic, source-agnostic speaker classification.
 *
 * Given the speaker labels attached to a captured thought (or atom), decide who
 * it is attributed to. This is the spoken / display-name counterpart to the
 * atomizer's RFC-2822 email resolver: that one keys correspondents off email
 * addresses; this one keys off speaker LABELS ("You", "Alex", "Speaker 1") and
 * a flag for machine-generated content (a device summary, an extracted item).
 *
 * Pure functions only — no database, no I/O — so the classification logic is
 * trivially testable (see ../test-attribution.mjs). The DB side (linking to a
 * self entity) lives in author-edges.mjs.
 *
 * attribution vocabulary:
 *   self     — only the brain owner spoke / authored it
 *   other    — only other NAMED people
 *   mixed    — the owner and at least one other named person
 *   machine  — generated by a device/model (no human speaker)
 *   unknown  — speech with no resolvable speaker (only generic "Speaker N")
 * ('external' is reserved for a future cross-source backfill and never emitted.)
 */

/** Device-generic labels for the brain owner. Callers add their own (e.g. their
 *  name) via the `selfLabels` option — nothing here is hardcoded to a person. */
export const DEFAULT_SELF_LABELS = ["you", "user", "me", "self", "myself"];

/** Labels that name no one in particular — diarizer placeholders. */
const GENERIC_SPEAKER_RE = /^(unknown|speaker[\s_]*\d+|spk[\s_]*\d+|user\s*\d+)$/i;

/** Build the self-label set: the device-generic defaults plus any extras
 *  (comma-separated string or array). Case-insensitive. */
export function selfLabelSet(extra = []) {
  const list = Array.isArray(extra)
    ? extra
    : String(extra || "").split(",");
  const cleaned = list.map((s) => String(s).trim().toLowerCase()).filter(Boolean);
  return new Set([...DEFAULT_SELF_LABELS, ...cleaned]);
}

export function isSelfSpeaker(name, selfLabels = selfLabelSet()) {
  return selfLabels.has(String(name || "").trim().toLowerCase());
}

export function isGenericSpeaker(name) {
  const s = String(name || "").trim();
  return !s || GENERIC_SPEAKER_RE.test(s);
}

/**
 * Classify a single speaker label into its kind. This is the resolver STUB:
 * it identifies WHAT a label is (self / a named other / a generic placeholder)
 * but does not resolve a named other to an entity row — that is deliberately
 * out of scope (see README: display-name -> entity resolution is a separate,
 * harder problem, just as the email resolver defers multi-address identity).
 */
export function resolveSpeaker(name, selfLabels = selfLabelSet()) {
  const label = String(name || "").trim();
  if (isSelfSpeaker(label, selfLabels)) return { kind: "self", label, normalized: label.toLowerCase() };
  if (isGenericSpeaker(label)) return { kind: "generic", label, normalized: label.toLowerCase() };
  return { kind: "named", label, normalized: label.toLowerCase() };
}

/**
 * Classify a set of speaker labels for one thought/atom.
 *
 * @param {string[]} speakers   speaker labels present in this thought
 * @param {object}   opts
 * @param {Set|string|string[]} [opts.selfLabels]  extra self labels (or a prebuilt Set)
 * @param {boolean}  [opts.hasUtterances=true]     false => machine-generated (a
 *        device summary / extracted item with no human speech)
 * @returns {{attribution, selfPresent, role}} role is the owner's thought_entities
 *          mention_role ('author' when self-only, 'participant' when mixed, else null)
 */
export function classifySpeakers(speakers = [], opts = {}) {
  const selfLabels = opts.selfLabels instanceof Set
    ? opts.selfLabels
    : selfLabelSet(opts.selfLabels);
  const hasUtterances = opts.hasUtterances !== false;

  if (!hasUtterances) return { attribution: "machine", selfPresent: false, role: null };

  let hasSelf = false;
  let hasNamedOther = false;
  for (const s of speakers) {
    if (isSelfSpeaker(s, selfLabels)) hasSelf = true;
    else if (!isGenericSpeaker(s)) hasNamedOther = true;
  }

  let attribution;
  if (hasSelf && hasNamedOther) attribution = "mixed";
  else if (hasSelf) attribution = "self";
  else if (hasNamedOther) attribution = "other";
  else attribution = "unknown";

  return { attribution, selfPresent: hasSelf, role: roleFromAttribution(attribution) };
}

/**
 * The owner's `thought_entities.mention_role` implied by an attribution label,
 * for paths that know the attribution but not the raw speaker list (e.g. a
 * backfill over rows that recorded `metadata.attribution` but not the speakers).
 *   self  -> author        mixed -> participant       otherwise -> null
 */
export function roleFromAttribution(attribution) {
  if (attribution === "self") return "author";
  if (attribution === "mixed") return "participant";
  return null;
}
